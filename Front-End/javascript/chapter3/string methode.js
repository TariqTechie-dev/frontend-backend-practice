// what is string methode?
// string methode is a method that is used to manipulate strings in JavaScript.
// It allows you to perform various operations on strings, such as searching, replacing, and formatting.
// Here are some common string methods in JavaScript:

let str = "  hellow   ";
// 1. `toUpperCase()`: Converts a string to uppercase.

console.log(str.toUpperCase());

// 2. `toLowerCase()`: Converts a string to lowercase.


// 3. `charAt(index)`: Returns the character at the specified index in a string.


// 4. `substring(start, end)`: Returns a substring of a string between the specified start and end indices.


// 5. `indexOf(searchValue)`: Returns the index of the first occurrence of a specified value in a string.

console.log(str.indexOf("w"));

// 6. `replace(searchValue, replaceValue)`: Replaces a specified value in a string with another value.


// 7. `split(separator)`: Splits a string into an array of substrings based on a specified separator.


// 8. `trim()`: Removes whitespace from both ends of a string.
console.log(str.trim());


// 9. `length`: Returns the length of a string.


// 10. `includes(searchValue)`: Checks if a string contains a specified value and returns true or false.


// 11. `startsWith(searchValue)`: Checks if a string starts with a specified value and returns true or false.


// 12. `endsWith(searchValue)`: Checks if a string ends with a specified value and returns true or false.


// 13. `repeat(count)`: Returns a new string that repeats the original string a specified number of times.


// 14. `concat(string1, string2, ...)`: Concatenates two or more strings and returns the result.


// 15. `search(regexp)`: Searches for a match between a regular expression and a string and returns the index of the match.
